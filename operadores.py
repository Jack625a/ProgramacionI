#Operadores en Python
print("OPERDARES EN PYTHON")
#Operdores Aritmeticos
#Suma - Resta - Multiplicacion - Division - Potencia
print("1. Operadores Aritemeticos")
numero1=45
numero2=75.55
suma=numero1+numero2
resta=numero1-numero2
multiplicacion=numero1*numero2
division=numero1/numero2
potencia=numero1**2
print("La suma de ",numero1,"+",numero2,"= ",suma)
print("La resta de ",numero1,"-",numero2,"= ",resta)
print("La multiplicacion de ",numero1,"x",numero2,"= ",multiplicacion)
print("La division de ",numero1,"/",numero2,"= ",division)
print(numero1, "Elevado al cuadrado es: ", potencia)
#Operadores de las cadenas de caracteres
print("2. Operadores de las cadenas de caracteres")
#Concatenacion = + ,
nombre="Kevin"
apellido="Arroyo"
nombreCompleto=nombre+" "+apellido
print(nombreCompleto)
print("Hola ",nombre," ",apellido)
#Cambio de linea \n
print("Hola",nombre,"\n",apellido)
#Tabulador \t
print("\t", nombreCompleto)
